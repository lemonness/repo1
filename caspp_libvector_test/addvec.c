int addcnt=0;

/**************************************************************************************************************
addvec :
add two vec to a new vector  and return a pointer
*********************************************************************************************************/
void addvec(int *x,int *y,int *z,int n){
    int i;
    addcnt++;
    for (i=0;i<n;i++){
        z[i]=x[i]+y[i];
    }
}